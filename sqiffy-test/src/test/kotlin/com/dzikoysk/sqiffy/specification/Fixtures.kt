            name = "types",
            properties = [
                Property(name = "id", type = SERIAL),
                Property(name = "label", type = VARCHAR, details = "64"),
                Property(name = "amount", type = INT),
                Property(name = "counter", type = LONG),
                Property(name = "ratio", type = DOUBLE),
                Property(name = "flag", type = BOOLEAN),
                Property(name = "day", type = DATE),
                Property(name = "moment", type = DATETIME),
                Property(name = "instant", type = TIMESTAMP),
            ]
        )
    ]
)
internal object TypesDefinition

