interface HasLabel {
    val label: String
}

/** A hand-written enum already on the classpath — referenced via [PriorityDefinition], never generated. */
enum class Priority {
    LOW, HIGH
}

/** Maps the `priority` column onto the existing [Priority] enum (validated against [Priority]'s constants). */
@EnumDefinition(
    name = "priority",
    mappedFrom = Priority::class,
    value = [EnumVersion(version = "1.0.0", operation = ADD_VALUES, values = ["LOW", "HIGH"])]
)
internal object PriorityDefinition

/**
 * A table whose generated entity implements a user-provided interface and maps an enum column onto an
 * existing (`mappedFrom`) enum.
 */
@Definition(
    implements = [HasLabel::class],
    versions = [
        DefinitionVersion(
            version = "1.0.0",
            name = "tagged",
            properties = [
                Property(name = "id", type = SERIAL),
                Property(name = "label", type = VARCHAR, details = "32"),
                Property(name = "priority", type = ENUM, enumDefinition = PriorityDefinition::class, nullable = true),
            ]
        )
    ]
)
internal object TaggedDefinition
